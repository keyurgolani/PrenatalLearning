/**
 * Get exercises by type
 */
export function getNumbersExercisesByType(type: Exercise['type']): Exercise[] {
  return numbersExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getNumbersTotalExerciseDuration(): number {
  return numbersExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getNumbersRecommendedSequence(): Exercise[] {
  return [
    numbersExercises.find(e => e.title === 'Fibonacci Breathing')!,
    numbersExercises.find(e => e.title === 'The Fibonacci Garden')!,
    numbersExercises.find(e => e.title === 'Patterns in Your World')!,
  ];
}

