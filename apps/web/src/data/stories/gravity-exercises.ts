/**
 * Get exercises by type
 */
export function getGravityExercisesByType(type: Exercise['type']): Exercise[] {
  return gravityExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getGravityTotalExerciseDuration(): number {
  return gravityExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getGravityRecommendedSequence(): Exercise[] {
  return [
    gravityExercises.find(e => e.title === 'Grounded Breathing')!,
    gravityExercises.find(e => e.title === 'The Fabric of Spacetime')!,
    gravityExercises.find(e => e.title === 'Being Held')!,
  ];
}

