/**
 * Get exercises by type
 */
export function getQuantumExercisesByType(type: Exercise['type']): Exercise[] {
  return quantumExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getQuantumTotalExerciseDuration(): number {
  return quantumExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getQuantumRecommendedSequence(): Exercise[] {
  return [
    quantumExercises.find(e => e.title === 'Superposition Breathing')!,
    quantumExercises.find(e => e.title === 'The Quantum Garden')!,
    quantumExercises.find(e => e.title === 'Embracing Uncertainty')!,
  ];
}

