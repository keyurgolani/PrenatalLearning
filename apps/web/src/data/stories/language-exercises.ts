/**
 * Get exercises by type
 */
export function getLanguageExercisesByType(type: Exercise['type']): Exercise[] {
  return languageExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getLanguageTotalExerciseDuration(): number {
  return languageExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getLanguageRecommendedSequence(): Exercise[] {
  return [
    languageExercises.find(e => e.title === 'The Sounds of Language')!,
    languageExercises.find(e => e.title === 'The River of Words')!,
    languageExercises.find(e => e.title === 'Words That Shaped You')!,
  ];
}

