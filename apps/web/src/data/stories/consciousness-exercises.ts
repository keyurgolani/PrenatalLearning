/**
 * Get exercises by type
 */
export function getConsciousnessExercisesByType(type: Exercise['type']): Exercise[] {
  return consciousnessExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getConsciousnessTotalExerciseDuration(): number {
  return consciousnessExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getConsciousnessRecommendedSequence(): Exercise[] {
  return [
    consciousnessExercises.find(e => e.title === 'Awareness of Awareness')!,
    consciousnessExercises.find(e => e.title === 'The Theater of the Mind')!,
    consciousnessExercises.find(e => e.title === 'The Preciousness of Experience')!,
  ];
}

