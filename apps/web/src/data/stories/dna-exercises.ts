/**
 * Get DNA exercises by type
 */
export function getDNAExercisesByType(type: Exercise['type']): Exercise[] {
  return dnaExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all DNA exercises
 */
export function getDNATotalExerciseDuration(): number {
  return dnaExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a DNA session
 */
export function getDNARecommendedSequence(): Exercise[] {
  return [
    dnaExercises.find(e => e.title === 'Breathing with the Code of Life')!,
    dnaExercises.find(e => e.title === 'The Dance of Replication')!,
    dnaExercises.find(e => e.title === 'The Gifts of Inheritance')!,
  ];
}

export default dnaExercises;
