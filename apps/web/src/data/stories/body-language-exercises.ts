/**
 * Get exercises by type
 */
export function getBodyLanguageExercisesByType(type: Exercise['type']): Exercise[] {
  return bodyLanguageExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getBodyLanguageTotalExerciseDuration(): number {
  return bodyLanguageExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getBodyLanguageRecommendedSequence(): Exercise[] {
  return [
    bodyLanguageExercises.find(e => e.title === 'The Silent Observer')!,
    bodyLanguageExercises.find(e => e.title === 'Body Awareness Breath')!,
    bodyLanguageExercises.find(e => e.title === 'The Mirror of Connection')!,
  ];
}

