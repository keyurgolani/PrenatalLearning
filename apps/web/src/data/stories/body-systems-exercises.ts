/**
 * Get Body Systems exercises by type
 */
export function getBodySystemsExercisesByType(type: Exercise['type']): Exercise[] {
  return bodySystemsExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all Body Systems exercises
 */
export function getBodySystemsTotalExerciseDuration(): number {
  return bodySystemsExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a Body Systems session
 */
export function getBodySystemsRecommendedSequence(): Exercise[] {
  return [
    bodySystemsExercises.find(e => e.title === 'Symphony Breathing')!,
    bodySystemsExercises.find(e => e.title === 'The Concert Hall Within')!,
    bodySystemsExercises.find(e => e.title === 'Listening to Your Inner Orchestra')!,
  ];
}

