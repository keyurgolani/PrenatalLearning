/**
 * Get Diversity exercises by type
 */
export function getDiversityExercisesByType(type: Exercise['type']): Exercise[] {
  return diversityExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all Diversity exercises
 */
export function getDiversityTotalExerciseDuration(): number {
  return diversityExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a Diversity session
 */
export function getDiversityRecommendedSequence(): Exercise[] {
  return [
    diversityExercises.find(e => e.title === 'Breathing with the Human Family')!,
    diversityExercises.find(e => e.title === 'Journey Around the World')!,
    diversityExercises.find(e => e.title === 'Your Cultural Heritage')!,
  ];
}

