/**
 * Get exercises by type
 */
export function getArtExercisesByType(type: Exercise['type']): Exercise[] {
  return artExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getArtTotalExerciseDuration(): number {
  return artExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getArtRecommendedSequence(): Exercise[] {
  return [
    artExercises.find(e => e.title === 'Breathing with Beauty')!,
    artExercises.find(e => e.title === 'What Beauty Means to You')!,
    artExercises.find(e => e.title === 'Your Creative Expression')!,
  ];
}

export default artExercises;
