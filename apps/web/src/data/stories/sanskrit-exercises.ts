/**
 * Get exercises by type
 */
export function getSanskritExercisesByType(type: Exercise['type']): Exercise[] {
  return sanskritExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getSanskritTotalExerciseDuration(): number {
  return sanskritExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getSanskritRecommendedSequence(): Exercise[] {
  return [
    sanskritExercises.find(e => e.title === 'Sanskrit Sound Journey')!,
    sanskritExercises.find(e => e.title === 'The LEGO Workshop of Language')!,
    sanskritExercises.find(e => e.title === 'Comparing Languages: Structure and Flexibility')!,
  ];
}

