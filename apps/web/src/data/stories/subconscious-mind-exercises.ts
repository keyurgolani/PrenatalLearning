// Helper functions
export function getSubconsciousMindExercisesByType(type: Exercise['type']): Exercise[] {
  return subconsciousMindExercises.filter(ex => ex.type === type);
}

export function getSubconsciousMindTotalExerciseDuration(): number {
  return subconsciousMindExercises.reduce((total, ex) => total + ex.duration, 0);
}

export function getSubconsciousMindRecommendedSequence(): Exercise[] {
  return [
    subconsciousMindExercises.find(e => e.title === 'Twilight State Induction')!,
    subconsciousMindExercises.find(e => e.title === 'The Inner Garden')!,
    subconsciousMindExercises.find(e => e.title === 'Garden Inventory')!,
  ];
}
