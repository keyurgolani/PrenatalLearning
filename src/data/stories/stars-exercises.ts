/**
 * Get exercises by type
 */
export function getStarsExercisesByType(type: Exercise['type']): Exercise[] {
  return starsExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getStarsTotalExerciseDuration(): number {
  return starsExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getStarsRecommendedSequence(): Exercise[] {
  return [
    starsExercises.find(e => e.title === 'Breathing with Starlight')!,
    starsExercises.find(e => e.title === 'The Life of a Star')!,
    starsExercises.find(e => e.title === 'Your Stellar Heritage')!,
  ];
}

