/**
 * Get Democracy exercises by type
 */
export function getDemocracyExercisesByType(type: Exercise['type']): Exercise[] {
  return democracyExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all Democracy exercises
 */
export function getDemocracyTotalExerciseDuration(): number {
  return democracyExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a Democracy session
 */
export function getDemocracyRecommendedSequence(): Exercise[] {
  return [
    democracyExercises.find(e => e.title === 'Breathing with the Community')!,
    democracyExercises.find(e => e.title === 'The Power of Your Vote')!,
    democracyExercises.find(e => e.title === 'Your Place in the Community')!,
  ];
}

