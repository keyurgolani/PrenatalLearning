/**
 * Get exercises by type
 */
export function getInfinityExercisesByType(type: Exercise['type']): Exercise[] {
  return infinityExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getInfinityTotalExerciseDuration(): number {
  return infinityExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getInfinityRecommendedSequence(): Exercise[] {
  return [
    infinityExercises.find(e => e.title === 'Breathing into Boundlessness')!,
    infinityExercises.find(e => e.title === 'The Infinite Staircase')!,
    infinityExercises.find(e => e.title === 'Personal Infinities')!,
  ];
}

