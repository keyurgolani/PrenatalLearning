/**
 * Get exercises by type
 */
export function getMusicExercisesByType(type: Exercise['type']): Exercise[] {
  return musicExercises.filter(exercise => exercise.type === type);
}

/**
 * Get total duration of all exercises
 */
export function getMusicTotalExerciseDuration(): number {
  return musicExercises.reduce((total, exercise) => total + exercise.duration, 0);
}

/**
 * Get a recommended exercise sequence for a session
 */
export function getMusicRecommendedSequence(): Exercise[] {
  return [
    musicExercises.find(e => e.title === 'Humming Lullaby')!,
    musicExercises.find(e => e.title === 'The River of Music')!,
    musicExercises.find(e => e.title === 'Music That Moves You')!,
  ];
}

